package org.vxinv.java_base.a7_common_class.c34_random;

// 7.6 随机
public class Pair {

    public Object item;
    public int weight;

    public Pair(Object item, int weight) {
        this.item = item;
        this.weight = weight;
    }

    public Object getItem() {
        return item;
    }

    public int getWeight() {
        return weight;
    }

}
